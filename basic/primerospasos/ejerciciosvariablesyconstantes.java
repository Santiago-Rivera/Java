package basic.primerospasos;

public class ejerciciosvariablesyconstantes {
    public static void main(String[] args) {
        // Declaración de variables
        int edad = 25; // Variable de tipo entero
        double altura = 1.75; // Variable de tipo decimal
        char inicial = 'J'; // Variable de tipo carácter
        boolean esEstudiante = true; // Variable de tipo booleano

        // Imprimir las variables
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura);
        System.out.println("Inicial: " + inicial);
        System.out.println("Es estudiante: " + esEstudiante);

        // Declaración de una constante
        final String NOMBRE = "Juan"; // Constante de tipo String

        // Imprimir la constante
        System.out.println("Nombre: " + NOMBRE);
    }
}
