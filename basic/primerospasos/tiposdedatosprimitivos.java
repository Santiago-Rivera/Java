package basic.primerospasos;

public class tiposdedatosprimitivos {
    public static void main(String[] args) {
        // Declaración de variables de tipo primitivo
        int numeroEntero = 10; // Tipo entero
        double numeroDecimal = 3.14; // Tipo decimal
        char caracter = 'A'; // Tipo carácter
        boolean esVerdadero = true; // Tipo booleano

        // Imprimir las variables
        System.out.println("Número entero: " + numeroEntero);
        System.out.println("Número decimal: " + numeroDecimal);
        System.out.println("Carácter: " + caracter);
        System.out.println("Es verdadero: " + esVerdadero);
    }
}
