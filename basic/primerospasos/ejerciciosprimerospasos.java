package basic.primerospasos;

public class ejerciciosprimerospasos {
    public static void main(String[] args) {
        // Ejercicio 1: Imprimir "Hola, Java!"
        System.out.println("Hola, Java!");

        // Ejercicio 2: Imprimir números del 1 al 5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // Ejercicio 3: Sumar dos números
        int a = 10;
        int b = 20;
        int suma = a + b;
        System.out.println("La suma de " + a + " y " + b + " es: " + suma);
    }
}
