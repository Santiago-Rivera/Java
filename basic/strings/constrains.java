package basic.strings;

public class constrains {
    public static void main(String[] args) {
        // Declaración de una cadena
        String texto = "Hola, Mundo";

        // Convertir a mayúsculas
        String mayusculas = texto.toUpperCase();

        // Imprimir la cadena en mayúsculas
        System.out.println("Cadena en mayúsculas: " + mayusculas); // Imprime: HOLA, MUNDO

        // Convertir a minúsculas
        String minusculas = texto.toLowerCase();

        // Imprimir la cadena en minúsculas
        System.out.println("Cadena en minúsculas: " + minusculas); // Imprime: hola, mundo
    }
}
