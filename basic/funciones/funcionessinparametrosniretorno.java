package basic.funciones;

public class funcionessinparametrosniretorno {
    public static void main(String[] args) {
        // Llamada a la función
        saludar();
        despedir();
        mostrarMensaje();
    }

    // Definición de la función sin parámetros ni retorno
    public static void saludar() {
        System.out.println("¡Hola, mundo!");
    }

    // Definición de otra función sin parámetros ni retorno
    public static void despedir() {
        System.out.println("¡Adiós, mundo!");
    }

    // Definición de una tercera función sin parámetros ni retorno
    public static void mostrarMensaje() {
        System.out.println("Este es un mensaje de prueba.");
    }
}
